$(function(){
    var ali=$("#ali").hide()
    var bli=$("#bli").hide()
    var cli=$("#cli").hide()
    var dli=$("#dli").hide()
    var aoption=$("#aoption").hide()
    var boption=$("#boption").hide()
    var coption=$("#coption").hide()
    var doption=$("#doption").hide()
   
    $("#a").focusout(()=>{
        // alert("focus out")
        var a=$("#a").val()
        ali.html("<small>a)"+a+"</small>")
        aoption.html(a).show()
        ali.show()
    })
    $("#b").focusout(()=>{
        // alert("focus out")
        var b=$("#b").val()
        bli.html("<small>b)"+b+"</small>")
        boption.html(b).show()
        bli.show()
    })
    $("#c").focusout(()=>{
        // alert("focus out")
        var c=$("#c").val()
        cli.html("<small>c)"+c+"</small>")
        coption.html(c).show()
        cli.show()
    })
    $("#d").focusout(()=>{
        // alert("focus out")
        var d=$("#d").val()
        dli.html("<small>d)"+d+"</small>")
        doption.html(d).show()
        dli.show()
    })
    $("#mediumqnshow").click(()=>{
        medium.show()
        low.hide()
        heigh.hide()
        advance.hide()
    })
    var medium=$(".medium").hide()
    var low=$(".low").hide()
    var heigh=$(".heigh").hide()
    var advance=$(".advance").hide()
    $("#lowqnshow").click(()=>{
        low.show()
        medium.hide()
        heigh.hide()
        advance.hide()
    })
    $("#highqnshow").click(()=>{
        heigh.show()
        medium.hide()
        low.hide()
        advance.hide()
    })
    $("#advanceqnshow").click(()=>{
        advance.show()
        low.hide()
        heigh.hide()
        medium.hide()
    })
    // var mview1=$("#mview1").hide()
    
    // $("#mview").click(()=>{
    //     $("#mview").show()
    // })
})